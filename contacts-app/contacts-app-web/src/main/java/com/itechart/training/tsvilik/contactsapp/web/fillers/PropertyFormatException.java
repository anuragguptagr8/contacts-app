package com.itechart.training.tsvilik.contactsapp.web.fillers;

public class PropertyFormatException extends Exception {

	private static final long serialVersionUID = -2313319558534038237L;

	public PropertyFormatException() {
	}

	public PropertyFormatException(String message) {
		super(message);
	}

	public PropertyFormatException(Throwable cause) {
		super(cause);
	}

	public PropertyFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public PropertyFormatException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
